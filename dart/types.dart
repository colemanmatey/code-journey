void main() {
  // Strings
  String firstname = "Coleman";
  print("String: $firstname");

  // Integers
  int num = 29;
  print("Integer: $num");

  // Double
  double decimal = 9.95;
  print("Double: $decimal");

  // Booleans
  bool isMarried = false;
  print(isMarried);

  // Dynamic
  dynamic school = "Spring Haven Academy";
  print("Dynamic: $school");
}
